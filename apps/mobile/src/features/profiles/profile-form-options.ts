export function getSelectLabel(
  value: string | null | undefined,
  options: SelectOption[],
  fallback = "Da completare",
) {
  if (!value) {
    return fallback;
  }

  return (
    options.find((option) => option.value === value || option.label === value)?.label ??
    value
  );
}
