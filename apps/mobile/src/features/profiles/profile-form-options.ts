function formatNationalityLabel(code: string) {
  return regionNames?.of(code) ?? code;
}

export const nationalityOptions: SelectOption[] = nationalityCodes
  .map((code) => ({
    label: formatNationalityLabel(code),
    value: code,
  }))
  .sort((left, right) => left.label.localeCompare(right.label, "it"));

